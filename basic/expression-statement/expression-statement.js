// Statement → performs action (e.g., variable declaration)
let x = 1
console.log(x) // 1

// Expression → produces value (e.g., arithmetic)
const y = (2 + 3) * 4
console.log(y) // 20

// Function call expression → returns value
function add(a, b) { return a + b }
console.log(add(1, 2)) // 3

// Assignment expression → sets value and returns assigned value
console.log(x = 10) // 10