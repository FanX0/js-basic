console.log(Math.asin(1))
// Math.acos(x) → arccosine
console.log(Math.acos(1))
// Math.atan(x) → arctangent
console.log(Math.atan(1))
// Math.atan2(y, x) → angle from coordinates
console.log(Math.atan2(1, 1))